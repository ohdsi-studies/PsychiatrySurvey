--Count of Age bucketed by 10 year increments starting at 18 (18-28, 28-38, 38-48, 48-58, 58-68, 68-78, 78-88, 88+):

with age_getter as (
  Select 
    person_id,
    datediff(year, p.birth_datetime, current_timestamp()) as age
  from ${omop_catalog}.cdm.person p
),
age_bucketer as (
Select 
  person_id,
  case  
    when age < 18 
      then '<18'
    when age >= 18 and age <= 28
      then '18-28'
    when age > 28 and age <= 38
      then '28-38'
    when age > 38 and age <= 48
      then '38-48'
    when age > 48 and age <= 58
      then '48-58'
    when age > 58 and age <= 68
      then '58-68'
    when age > 68 and age <= 78
      then '68-78'
    when age > 78 and age <= 88
      then '78-88'
    else '88+'
  end as age_buckets
from age_getter
)
Select
   count(person_id) as count_of_persons,
   age_buckets
from age_bucketer
group by age_buckets
order by count_of_persons desc




